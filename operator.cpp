//This program implies the use of operstors

#include<iostream>
using namespace std;

int main()
{
    int a,b;

    cout<<"Enter the value of a"<<endl;
    cin>>a;

    cout<<"Enter the value of b"<<endl;
    cin>>b;

    //printing addition
   cout<<"The sum of the numbers a and b is "<<a + b<<endl;
   //printing difference
   cout<<"The difference of the numbers a and b is "<<a - b<<endl;
   //printing multiplication
   cout<<"The multiplication of the numbers a and b is "<<a * b<<endl;
   //printing division
   cout<<"The division of the numbers a and b is "<<(float)a / b<<endl;
}